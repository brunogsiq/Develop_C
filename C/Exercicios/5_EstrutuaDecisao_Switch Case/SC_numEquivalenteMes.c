#include <stdio.h>
#include <stdlib.h>

/*Faça um algoritmo que leia um número e mostre a qual mês ele corresponde. Se o
valor for maior do que 12 ou menor do que 1, diga “Valor não corresponde a nenhum
mês”. */

int main()
{
    int mes;

    printf("Digite um número correspondente a um mes :\n");
    scanf("%d", &mes);

    switch (mes)
    {
    case 1:
    printf("Janeiro");
    break;
    case 2:
    printf("Fevereiro");
    break;
    case 3:
    printf("Março");
    break;
    case 4:
    printf("Abril");
    break;
    case 5:
    printf("Maio");
    break;
    case 6:
    printf("Junho");
    break;
    case 7:
    printf("Julho");
    break;
    case 8:
    printf("Agosto");
    break;
    case 9:
    printf("Setembro");
    break;
    case 10:
    printf("Outubro");
    break;
    case 11:
    printf("Novembro");
    break;
    case 12:
    printf("Dezembro");
    break;
    default:
    printf("Valor não corresponde a nenhum mes");

    }
    return 0;
}
